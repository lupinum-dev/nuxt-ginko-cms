#!/usr/bin/env node
import { mkdir, readFile, writeFile } from 'node:fs/promises'
import { dirname, resolve } from 'node:path'

const MODULE_PACKAGE_NAME = 'nuxt-ginko-cms'

function printHelp() {
  console.log(`ginko CLI

Usage:
  ginko sync-types [--out types/ginko.generated.ts] [--module-out types/ginko.module.d.ts] [--base https://site.ginko-cms.com]
  ginko check [--out types/ginko.generated.ts] [--module-out types/ginko.module.d.ts] [--base https://site.ginko-cms.com]
  ginko init-site [--nuxt-config nuxt.config.ts]

Env:
  NUXT_GINKO_CMS_KEY   required API key (for sync/check)
  NUXT_GINKO_CMS_BASE  optional upstream base (default https://site.ginko-cms.com)
  NUXT_GINKO_CMS_SCHEMA_URL optional absolute/path schema endpoint override
`)
}

function argValue(args, key) {
  const index = args.indexOf(key)
  if (index === -1) {
    return undefined
  }
  return args[index + 1]
}

function normalizeBase(base) {
  const parsed = new URL(base)
  parsed.pathname = '/'
  parsed.search = ''
  parsed.hash = ''
  return parsed.toString()
}

async function fetchJson(url, key) {
  const response = await fetch(url, {
    headers: {
      Authorization: `Bearer ${key}`,
      Accept: 'application/json',
    },
  })

  if (!response.ok) {
    const body = await response.text().catch(() => '')
    throw new Error(`Request failed (${response.status}) for ${url}: ${body.slice(0, 240)}`)
  }

  return await response.json()
}

function toSchemaEndpoint(base, value) {
  const raw = String(value || '').trim()
  if (!raw) {
    return undefined
  }

  if (raw.startsWith('http://') || raw.startsWith('https://')) {
    return raw
  }

  const normalizedPath = raw.startsWith('/') ? raw : `/${raw}`
  return new URL(normalizedPath, base).toString()
}

function sortObjectKeys(input) {
  if (Array.isArray(input)) {
    return input.map(sortObjectKeys)
  }

  if (!input || typeof input !== 'object') {
    return input
  }

  return Object.fromEntries(
    Object.entries(input)
      .sort(([a], [b]) => a.localeCompare(b))
      .map(([key, value]) => [key, sortObjectKeys(value)]),
  )
}

function stableJson(input) {
  return JSON.stringify(sortObjectKeys(input), null, 2)
}

async function fetchContext(base, key) {
  const contextUrl = new URL('/api/v2/cms/context', base)
  const contextResponse = await fetchJson(contextUrl.toString(), key)
  const context = contextResponse?.data || {}
  const teamSlug = context?.team?.slug
  if (!teamSlug) {
    throw new Error('Context response missing team slug')
  }

  return {
    context,
    teamSlug,
  }
}

async function fetchSchemaRows(base, key, teamSlug) {
  const primarySchemaUrl = new URL(`/api/v1/cms/${teamSlug}/schema`, base).toString()
  const overrideSchemaUrl = toSchemaEndpoint(base, process.env.NUXT_GINKO_CMS_SCHEMA_URL)
  const endpoints = [primarySchemaUrl]

  if (overrideSchemaUrl && overrideSchemaUrl !== primarySchemaUrl) {
    endpoints.push(overrideSchemaUrl)
  }

  const failures = []
  for (const endpoint of endpoints) {
    try {
      const schemaResponse = await fetchJson(endpoint, key)
      return Array.isArray(schemaResponse?.data) ? schemaResponse.data : []
    }
    catch (error) {
      const message = error instanceof Error ? error.message : String(error)
      failures.push({ endpoint, message })
    }
  }

  const details = failures
    .map(({ endpoint, message }) => `- ${endpoint}: ${message}`)
    .join('\n')

  throw new Error(
    `Failed to fetch schema from configured endpoint(s).\n${details}\n`
    + 'Set NUXT_GINKO_CMS_SCHEMA_URL to override schema endpoint if needed.',
  )
}

function renderTypesOutput(context, schemas) {
  return `/* eslint-disable */\n/**\n * Generated by \`ginko sync-types\`\n * Do not edit manually.\n * This file mirrors the raw upstream Ginko schema and context for the current team.\n */\n\nexport const ginkoContext = ${stableJson(context)} as const\n\nexport const ginkoCollectionSchemas = ${stableJson(schemas)} as const\n\nexport type GinkoCollectionSlug = keyof typeof ginkoCollectionSchemas\n\nexport type GinkoSchemaField = {\n  key: string\n  label: string\n  type: string\n  required: boolean\n  localized: boolean\n  config?: Record<string, unknown>\n}\n\nexport type GinkoGeneratedCollection = {\n  slug: string\n  name: string\n  fields: readonly GinkoSchemaField[]\n}\n\nexport type GinkoGeneratedSchemaMap = typeof ginkoCollectionSchemas\nexport type GinkoGeneratedContext = typeof ginkoContext\n`
}

function normalizeSiteConfig(rawSite) {
  if (!rawSite || typeof rawSite !== 'object' || Array.isArray(rawSite)) {
    throw new Error('Missing ginkoCms.site configuration in nuxt.config.ts')
  }

  const locales = Array.isArray(rawSite.locales)
    ? rawSite.locales
        .map((locale) => {
          if (!locale || typeof locale !== 'object') {
            return undefined
          }

          const code = typeof locale.code === 'string' ? locale.code.trim() : ''
          const hreflang = typeof locale.hreflang === 'string' ? locale.hreflang.trim() : ''
          if (!code || !hreflang) {
            return undefined
          }

          return {
            code,
            hreflang,
            isDefault: locale.isDefault === true,
          }
        })
        .filter(Boolean)
    : []

  if (!locales.length) {
    throw new Error('ginkoCms.site.locales must include at least one locale')
  }

  const defaultLocale = (typeof rawSite.defaultLocale === 'string' && rawSite.defaultLocale.trim())
    || locales.find(locale => locale.isDefault)?.code
    || locales[0]?.code

  if (!defaultLocale) {
    throw new Error('Could not resolve ginkoCms.site.defaultLocale')
  }

  const collectionsInput = rawSite.collections
  if (!collectionsInput || typeof collectionsInput !== 'object' || Array.isArray(collectionsInput)) {
    throw new Error('ginkoCms.site.collections must be an object keyed by collection key')
  }

  const collections = Object.fromEntries(
    Object.entries(collectionsInput).map(([key, collection]) => {
      if (!collection || typeof collection !== 'object' || Array.isArray(collection)) {
        throw new Error(`Invalid collection config for key: ${key}`)
      }

      const source = typeof collection.source === 'string' ? collection.source.trim() : ''
      if (!source) {
        throw new Error(`Collection "${key}" must define source`)
      }

      return [key, {
        kind: collection.kind === 'hierarchy' ? 'hierarchy' : 'flat',
        source,
      }]
    }),
  )

  return {
    defaultLocale,
    locales,
    collections,
  }
}

async function loadNuxtSiteConfig(cwd) {
  let loadNuxtConfig
  try {
    ;({ loadNuxtConfig } = await import('@nuxt/kit'))
  }
  catch {
    return undefined
  }

  const nuxtConfig = await loadNuxtConfig({ cwd })
  if (!nuxtConfig?.ginkoCms?.site) {
    return undefined
  }

  return normalizeSiteConfig(nuxtConfig.ginkoCms.site)
}

function toTypeLiteralKey(key) {
  return /^[$A-Z_][\w$]*$/i.test(key) ? key : JSON.stringify(key)
}

function mapFieldType(fieldType) {
  switch (fieldType) {
    case 'text':
    case 'slug':
    case 'richtext':
    case 'date':
    case 'datetime':
    case 'email':
    case 'url':
      return 'string'
    case 'number':
      return 'number'
    case 'boolean':
      return 'boolean'
    case 'image':
      return 'string | { src: string, [key: string]: unknown }'
    case 'relation':
      return 'Array<Record<string, unknown>>'
    case 'json':
    case 'object':
      return 'Record<string, unknown>'
    default:
      throw new Error(`Unsupported field type for strict generation: ${fieldType}`)
  }
}

function buildCollectionType(schema) {
  const fields = Array.isArray(schema?.fields) ? schema.fields : []
  const lines = [
    'id: string',
    'slug: string',
    'status: string',
    'updatedAt?: number',
    'path?: string',
  ]
  const reserved = new Set(['id', 'slug', 'status', 'updatedAt', 'path'])

  for (const field of fields) {
    const key = typeof field?.key === 'string' ? field.key : ''
    if (!key || reserved.has(key)) {
      continue
    }

    const fieldType = mapFieldType(String(field.type || ''))
    const optional = field.required === true ? '' : '?'
    lines.push(`${toTypeLiteralKey(key)}${optional}: ${fieldType}`)
  }

  return `{\n${lines.map(line => `    ${line}`).join('\n')}\n  }`
}

function resolveCollectionTypingTargets({ schemaMap, siteConfig }) {
  if (!siteConfig?.collections) {
    return Object.entries(schemaMap).map(([collectionKey, schema]) => ({
      collectionKey,
      schema,
    }))
  }

  return Object.entries(siteConfig.collections).map(([collectionKey, collection]) => ({
    collectionKey,
    schema: schemaMap[collection.source] || null,
  }))
}

function renderModuleAugmentationOutput({ targets }) {
  const interfaceLines = targets.map(({ collectionKey, schema }) => {
    const key = toTypeLiteralKey(collectionKey)
    if (!schema) {
      return `  ${key}: Record<string, unknown>`
    }

    return `  ${key}: ${buildCollectionType(schema)}`
  })

  return `/* eslint-disable */\n/**\n * Generated by \`ginko sync-types\`\n * Do not edit manually.\n * This file augments the local module collection keys that map to upstream sources.\n */\n\nimport '${MODULE_PACKAGE_NAME}'\nimport '${MODULE_PACKAGE_NAME}/runtime/types'\n\ninterface __GeneratedGinkoCollections {\n${interfaceLines.join('\n')}\n}\n\ndeclare module '${MODULE_PACKAGE_NAME}' {\n  interface GinkoCollections extends __GeneratedGinkoCollections {}\n}\n\ndeclare module '${MODULE_PACKAGE_NAME}/runtime/types' {\n  interface GinkoCollections extends __GeneratedGinkoCollections {}\n}\n\nexport {}\n`
}

async function getSyncTypesOutputs(args) {
  const outPath = resolve(process.cwd(), argValue(args, '--out') || 'types/ginko.generated.ts')
  const moduleOutPath = resolve(process.cwd(), argValue(args, '--module-out') || 'types/ginko.module.d.ts')
  const key = String(process.env.NUXT_GINKO_CMS_KEY || '').trim()
  if (!key) {
    throw new Error('Missing NUXT_GINKO_CMS_KEY')
  }

  const base = normalizeBase(argValue(args, '--base') || process.env.NUXT_GINKO_CMS_BASE || 'https://site.ginko-cms.com')
  const { context, teamSlug } = await fetchContext(base, key)

  const schemaRows = await fetchSchemaRows(base, key, teamSlug)
  const stableSchemaRows = [...schemaRows]
    .filter(schema => schema && typeof schema.slug === 'string' && schema.slug.length > 0)
    .sort((a, b) => String(a.slug).localeCompare(String(b.slug)))

  const schemaMap = Object.fromEntries(stableSchemaRows.map(schema => [schema.slug, schema]))
  const siteConfig = await loadNuxtSiteConfig(process.cwd())
  const targets = resolveCollectionTypingTargets({
    schemaMap,
    siteConfig,
  })

  return {
    outPath,
    moduleOutPath,
    output: renderTypesOutput(context, schemaMap),
    moduleOutput: renderModuleAugmentationOutput({ targets }),
  }
}

async function readExisting(path) {
  try {
    return await readFile(path, 'utf8')
  }
  catch {
    return undefined
  }
}

async function writeOutput(path, output) {
  await mkdir(dirname(path), { recursive: true })
  await writeFile(path, output, 'utf8')
}

async function runSyncTypes(args) {
  const outputs = await getSyncTypesOutputs(args)
  await writeOutput(outputs.outPath, outputs.output)
  await writeOutput(outputs.moduleOutPath, outputs.moduleOutput)
  console.log(`Generated ${outputs.outPath}`)
  console.log(`Generated ${outputs.moduleOutPath}`)
}

async function runCheck(args) {
  let hasDiff = false
  const outputs = await getSyncTypesOutputs(args)

  const existingRuntime = await readExisting(outputs.outPath)
  if (existingRuntime !== outputs.output) {
    hasDiff = true
    console.error(`Outdated generated file: ${outputs.outPath}`)
  }

  const existingModule = await readExisting(outputs.moduleOutPath)
  if (existingModule !== outputs.moduleOutput) {
    hasDiff = true
    console.error(`Outdated generated file: ${outputs.moduleOutPath}`)
  }

  if (hasDiff) {
    throw new Error('Generated files are out of date. Run `ginko sync-types` and commit the result.')
  }

  console.log('All generated files are up to date.')
}

function addScriptIfMissing(pkg, key, value) {
  pkg.scripts = pkg.scripts || {}
  if (!pkg.scripts[key]) {
    pkg.scripts[key] = value
  }
}

function injectSiteSnippet(source) {
  if (source.includes('ginkoCms:')) {
    return source
  }

  const defineIndex = source.indexOf('defineNuxtConfig({')
  if (defineIndex < 0) {
    return source
  }

  const closeIndex = source.lastIndexOf('})')
  if (closeIndex < 0 || closeIndex <= defineIndex) {
    return source
  }

  const snippet = `\n  ginkoCms: {\n    site: {\n      defaultLocale: 'de',\n      locales: [\n        { code: 'de', hreflang: 'de-DE', isDefault: true },\n      ],\n      routing: {\n        localePrefixStrategy: 'prefix_except_default',\n      },\n      staticRoutes: ['/'],\n      collections: {},\n      search: {\n        enabled: true,\n        defaultLimit: 12,\n      },\n      sitemap: {\n        enabled: true,\n        sourcePath: '/api/ginko/sitemap',\n      },\n    },\n  },\n\n`

  return `${source.slice(0, closeIndex)}${snippet}${source.slice(closeIndex)}`
}

async function runInitSite(args) {
  const packageJsonPath = resolve(process.cwd(), 'package.json')
  const packageJsonRaw = await readFile(packageJsonPath, 'utf8')
  const packageJson = JSON.parse(packageJsonRaw)

  addScriptIfMissing(packageJson, 'cms:types', 'ginko sync-types --out types/ginko.generated.ts --module-out types/ginko.module.d.ts')
  addScriptIfMissing(packageJson, 'cms:sync', 'ginko sync-types --out types/ginko.generated.ts --module-out types/ginko.module.d.ts')
  addScriptIfMissing(packageJson, 'cms:check', 'ginko check --out types/ginko.generated.ts --module-out types/ginko.module.d.ts')

  await writeOutput(packageJsonPath, `${JSON.stringify(packageJson, null, 2)}\n`)
  console.log(`Updated ${packageJsonPath}`)

  const nuxtConfigPath = resolve(process.cwd(), argValue(args, '--nuxt-config') || 'nuxt.config.ts')
  const nuxtConfigRaw = await readFile(nuxtConfigPath, 'utf8')
  const nextNuxtConfig = injectSiteSnippet(nuxtConfigRaw)

  if (nextNuxtConfig !== nuxtConfigRaw) {
    await writeOutput(nuxtConfigPath, nextNuxtConfig)
    console.log(`Updated ${nuxtConfigPath}`)
  }
  else {
    console.log(`Skipped ${nuxtConfigPath} (ginkoCms.site already present or unsupported file shape)`)
  }
}

async function main() {
  const args = process.argv.slice(2)
  const command = args[0]

  if (!command || command === '--help' || command === '-h') {
    printHelp()
    return
  }

  if (command === 'sync-types') {
    await runSyncTypes(args.slice(1))
    return
  }

  if (command === 'check') {
    await runCheck(args.slice(1))
    return
  }

  if (command === 'init-site') {
    await runInitSite(args.slice(1))
    return
  }

  throw new Error(`Unknown command: ${command}`)
}

main().catch((error) => {
  console.error(`[ginko] ${error.message}`)
  process.exit(1)
})
